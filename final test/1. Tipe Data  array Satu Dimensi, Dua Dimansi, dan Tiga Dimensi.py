# Array satu dimensi
array1D = [1, 2, 3, 4, 5]
print("Array 1 Dimensi:", array1D)

# Array dua dimensi
array2D = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]
print("\nArray 2 Dimensi:")
for row in array2D:
    print(row)

# Array tiga dimensi
array3D = [
    [
        [1, 2], [3, 4]
    ],
    [
        [5, 6], [7, 8]
    ]
]
print("\nArray 3 Dimensi:")
for block in array3D:
    print(block)

