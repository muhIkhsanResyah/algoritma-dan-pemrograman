# Tipe data dasar Python

# Integer
angka = 10

# Float
desimal = 3.14

# String
teks = "Hello World"

# Boolean
status = True

# List
daftar = [1, 2, 3, 4]

# Tuple
tup = (10, 20, 30)

# Set
himpunan = {1, 2, 3}

# Dictionary
kamus = {"nama": "Andi", "umur": 19}

print("Integer:", angka)
print("Float:", desimal)
print("String:", teks)
print("Boolean:", status)
print("List:", daftar)
print("Tuple:", tup)
print("Set:", himpunan)
print("Dictionary:", kamus)

